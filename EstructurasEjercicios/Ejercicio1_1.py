num1 = int(input("Enter the first number: "))
num2 = int(input("Enter the second number: "))

if num1 > num2:
    print("Sum between the numbers: ", num1+num2)
    print("Subtraction between the numbers: ", num1-num2)
else: 
    print("Product between the numbers: ", num1*num2)
    print("Division between the numbers: ", num1/num2)
